use std::borrow::Cow;

use rust_i18n::t;

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum Language {
    Chinese,
    English,
}

impl Language {
    #[must_use]
    pub fn label(self) -> &'static str {
        match self {
            Self::Chinese => "中文",
            Self::English => "English",
        }
    }

    #[must_use]
    pub fn locale(self) -> &'static str {
        match self {
            Self::Chinese => "zh_CN",
            Self::English => "en_US",
        }
    }
}

